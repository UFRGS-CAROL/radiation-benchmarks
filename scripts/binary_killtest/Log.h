#ifndef LOG_H_
#define LOG_H_

#include <string>

namespace radiation {

//!  Log class.
/*!
 This class is to log the messages
 generated by the watchdog process
 */
class Log {
private:
	//!  Log path.
	/*!
	 This string contains the log file path
	 */
	std::string log_path;

public:
	//!  Log constructor
	/*!
	 \param log_file_path the path to the log file
	 */
	Log(std::string log_file_path);

	//!  Log copy constructor.
	/*!
	 \param b the object to be copied
	 */
	Log(const Log& b);

	//!  Default constructor.
	/*!
	 */
	Log();

	//!  Log a message with exception.
	/*!
	 Log a message and throw an exception.
	 \param message to be logged
	 */
	void log_message_exception(std::string message);

	//!  Log a message
	/*!
	 Log a message
	 \param message to be logged
	 */
	void log_message_info(std::string message);
};

} /* namespace radiation */

#endif /* LOG_H_ */
